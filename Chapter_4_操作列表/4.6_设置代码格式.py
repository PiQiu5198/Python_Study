#缩进
#PEP8建议每级缩进都使用4个空格。
#在字处理文档中，大家常常用制表符（tab）而不是空格来缩进。
#但是在Python中混合使用制表符和空格会让Python解释器感到迷惑。

#行长
#很多Python程序员建议每行不超过80个字符。
#PEP8还建议注释的行长不超过72个字符
#因为有些工具为大型项目自动生成文档时，会在每行注释开头添加格式化字符。

#空行
#要将程序的不同部分分开，可使用空行。
#例如，如果有5行创建列表的代码，还有3行处理该列表的代码，用一个空行将这两部分分开合适。
#但是不应该使用三四给空行来将他们隔开。
#空行不会影响代码的运行，但是会影响代码的可读性。